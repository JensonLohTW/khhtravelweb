/**
 * 環境配置文件 (Environment Configuration)
 *
 * 用途：控制應用程式的運行環境（開發/生產模式）
 *
 * 使用方式：
 * 1. 開發時：設定 ENV = 'development'
 * 2. 部署上線前：設定 ENV = 'production'
 * 3. 或者直接註解掉整個文件的內容，讓系統自動判斷
 */

// ============================================
// 環境設定（Environment Setting）
// ============================================

/**
 * 環境模式設定
 * - 'development': 開發模式（顯示開發工具，如場景名稱編輯、設定匯出等）
 * - 'production': 生產模式（隱藏所有開發工具）
 * - null 或不設定: 自動判斷（localhost 為 development，其他為 production）
 */
window.MARZIPANO_APP_ENV = 'production';  // 👈 修改這裡來切換模式


// ============================================
// 快速切換指南
// ============================================
/*

📌 部署上線前的步驟：

方法一：設定為生產模式（推薦）
----------------------------------
將上面的設定改為：
window.MARZIPANO_APP_ENV = 'production';


方法二：完全註解掉（使用自動判斷）
----------------------------------
將上面的設定註解掉：
// window.MARZIPANO_APP_ENV = 'development';

這樣系統會自動判斷：
- localhost/127.0.0.1 → 開發模式
- 其他域名 → 生產模式


方法三：完全移除此文件（最簡單）
----------------------------------
部署時直接刪除或不載入 config.js
系統會使用默認的自動判斷邏輯

*/

// ============================================
// 開發模式功能說明
// ============================================
/*

當環境為 'development' 時，會啟用以下功能：

✅ 場景列表中的「匯出設定」按鈕
   - 用於匯出當前的場景名稱覆寫設定
   - 位置：場景列表頂部

✅ 場景名稱編輯功能（如果啟用）
   - 可以雙擊場景名稱進行編輯
   - 修改會保存在瀏覽器本地儲存

✅ 其他可能的開發工具
   - 未來可能新增的開發輔助功能


當環境為 'production' 時：

❌ 所有開發工具都會隱藏
❌ 使用者無法看到或訪問開發功能
✅ 應用呈現最終產品狀態

*/
